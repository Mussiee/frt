DateTime parseMockEventDateTime(String date, String time) {
  const months = <String, int>{
    'Jan': 1,
    'Feb': 2,
    'Mar': 3,
    'Apr': 4,
    'May': 5,
    'Jun': 6,
    'Jul': 7,
    'Aug': 8,
    'Sep': 9,
    'Oct': 10,
    'Nov': 11,
    'Dec': 12,
  };

  final dateParts = date.replaceAll(',', '').split(' ');
  if (dateParts.length != 3) {
    throw FormatException('Invalid event date format: $date');
  }

  final month = months[dateParts[0]];
  if (month == null) {
    throw FormatException('Invalid event month: ${dateParts[0]}');
  }

  final day = int.tryParse(dateParts[1]);
  final year = int.tryParse(dateParts[2]);
  if (day == null || year == null) {
    throw FormatException('Invalid event date values: $date');
  }

  final timeParts = time.split(' ');
  if (timeParts.length != 2) {
    throw FormatException('Invalid event time format: $time');
  }

  final clock = timeParts[0].split(':');
  if (clock.length != 2) {
    throw FormatException('Invalid event time value: $time');
  }

  var hour = int.tryParse(clock[0]);
  final minute = int.tryParse(clock[1]);
  if (hour == null || minute == null) {
    throw FormatException('Invalid event time numbers: $time');
  }

  final period = timeParts[1].toUpperCase();
  hour = hour % 12;
  if (period == 'PM') {
    hour += 12;
  }

  return DateTime(year, month, day, hour, minute);
}

extension MockEventDateTime on MockEvent {
  DateTime get startsAt => parseMockEventDateTime(date, time);
}

